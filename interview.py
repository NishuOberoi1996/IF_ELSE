num1=float(input("enter the number1:-"))
num2=float (input("enter the number2:-"))
operation=input("select operator:-")

if operation =="+":
    print(num1+num2)
elif operation=="-":
    print(num1-num2)
elif operation == "*":
    print(num1*num2)
elif operation =="//":
    print(num1//num2)
elif operation =="%":
    print(num1%num2)
elif operation =="/":
    print(num1/num2)
elif operation =="**":
    print(num1**num2)
elif operation =="!=":
    print(num1!=num2)
else:
    print("wrong data")




